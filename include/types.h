/**
 * @file types.h
 * @brief Основные типы данных проекта
 * 
 * Определяет основные типы данных, используемые в проекте:
 * - Vector для работы с целочисленными векторами
 * - ByteArray для работы с бинарными данными
 * 
 * @version 1.0
 * @date 2024
 */

#ifndef TYPES_H
#define TYPES_H

#include <cstdint>
#include <vector>

/**
 * @brief Тип для целочисленного вектора
 * 
 * Используется для передачи и обработки векторов целых чисел 32-битного формата.
 * Основной тип данных для вычислительных операций сервера.
 */
using Vector = std::vector<int32_t>;

/**
 * @brief Тип для массива байтов
 * 
 * Используется для работы с бинарными данными, хэшами и сетевыми пакетами.
 */
using ByteArray = std::vector<uint8_t>;

#endif
